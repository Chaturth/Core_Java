class Stabilizer {

    static String getBrand(String brand) {
        System.out.println("Brand of stabilizer: " + brand);
        return brand;
    }

    static int getCapacity(int capacity) {
        System.out.println("Capacity in VA: " + capacity);
        return capacity;
    }

    static String getColor(String color) {
        System.out.println("Color of stabilizer: " + color);
        return color;
    }

    static boolean isAutomatic(boolean automatic) {
        System.out.println("Is automatic: " + automatic);
        return automatic;
    }

    static int getPrice(int price) {
        System.out.println("Price of stabilizer: " + price);
        return price;
    }
}