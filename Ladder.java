class Ladder {

    static String getMaterial(String material) {
        System.out.println("Material of ladder: " + material);
        return material;
    }

    static int getHeight(int height) {
        System.out.println("Height in feet: " + height);
        return height;
    }

    static String getColor(String color) {
        System.out.println("Color of ladder: " + color);
        return color;
    }

    static boolean isFoldable(boolean foldable) {
        System.out.println("Is foldable: " + foldable);
        return foldable;
    }

    static int getSteps(int steps) {
        System.out.println("Number of steps: " + steps);
        return steps;
    }
}