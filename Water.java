class Water {

    static String getSource(String source) {
        System.out.println("Water source: " + source);
        return source;
    }

    static int getQuantity(int quantity) {
        System.out.println("Quantity in liters: " + quantity);
        return quantity;
    }

    static String getColor(String color) {
        System.out.println("Color of water: " + color);
        return color;
    }

    static boolean isDrinkable(boolean drinkable) {
        System.out.println("Is drinkable: " + drinkable);
        return drinkable;
    }

    static int getPH(int ph) {
        System.out.println("pH value: " + ph);
        return ph;
    }
}