class Bench {

    static String getMaterial(String material) {
        System.out.println("Material of bench: " + material);
        return material;
    }

    static int getLength(int length) {
        System.out.println("Length in cm: " + length);
        return length;
    }

    static String getColor(String color) {
        System.out.println("Color of bench: " + color);
        return color;
    }

    static boolean isWooden(boolean wooden) {
        System.out.println("Is wooden: " + wooden);
        return wooden;
    }

    static int getPrice(int price) {
        System.out.println("Price of bench: " + price);
        return price;
    }
}