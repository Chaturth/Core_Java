class Tablet {

    static String getName(String name) {
        System.out.println("Tablet name: " + name);
        return name;
    }

    static int getPrice(int price) {
        System.out.println("Price of tablet: " + price);
        return price;
    }

    static String getBrand(String brand) {
        System.out.println("Brand of tablet: " + brand);
        return brand;
    }

}  

