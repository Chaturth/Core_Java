class Bullet {

    static String getModel(String model) {
        System.out.println("Model is: " + model);
        return model;
    }

    static int getPrice(int price) {
        System.out.println("Price is: " + price);
        return price;
    }

    static int getEngineCC(int engineCC) {
        System.out.println("Engine CC is: " + engineCC);
        return engineCC;
    }

    static String getColor(String color) {
        System.out.println("Color is: " + color);
        return color;
    }

    static boolean hasABS(boolean abs) {
        System.out.println("ABS Available: " + abs);
        return abs;
    }
}