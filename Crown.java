class Crown {

    static String getMaterial(String material) {
        System.out.println("Material of crown: " + material);
        return material;
    }

    static int getWeight(int weight) {
        System.out.println("Weight in grams: " + weight);
        return weight;
    }

    static String getColor(String color) {
        System.out.println("Color of crown: " + color);
        return color;
    }

    static boolean hasGems(boolean gems) {
        System.out.println("Has precious gems: " + gems);
        return gems;
    }

    static int getPrice(int price) {
        System.out.println("Price of crown: " + price);
        return price;
    }
}