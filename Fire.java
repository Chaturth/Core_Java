class Fire {

    static String getType(String type) {
        System.out.println("Type of fire: " + type);
        return type;
    }

    static int getTemperature(int temperature) {
        System.out.println("Temperature is: " + temperature);
        return temperature;
    }

    static String getColor(String color) {
        System.out.println("Color of fire: " + color);
        return color;
    }

    static boolean isDangerous(boolean dangerous) {
        System.out.println("Is dangerous: " + dangerous);
        return dangerous;
    }

    static int getDuration(int duration) {
        System.out.println("Duration in minutes: " + duration);
        return duration;
    }
}