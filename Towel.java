class Towel {

    static String getBrand(String brand) {
        System.out.println("Brand of towel: " + brand);
        return brand;
    }

    static int getPrice(int price) {
        System.out.println("Price of towel: " + price);
        return price;
    }

    static String getColor(String color) {
        System.out.println("Color of towel: " + color);
        return color;
    }

    static boolean isCotton(boolean cotton) {
        System.out.println("Is cotton material: " + cotton);
        return cotton;
    }

    static int getLength(int length) {
        System.out.println("Length in cm: " + length);
        return length;
    }
}